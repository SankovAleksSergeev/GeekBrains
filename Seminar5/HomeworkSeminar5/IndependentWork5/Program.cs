void Print (int [,] number)
{
    for (int i=0; i<number.GetLength(0); i++)
    {
        for (int j=0; j<number.GetLength(1); j++)
        {
            Console.Write($"{number[i,j]} ");
        }
    Console.WriteLine();
    }
}


void Random (int [,] number)
{
    for (int i=0; i<number.GetLength(0); i++)
    {
        for (int j=0; j<number.GetLength(1); j++)
        {
            number[i,j]= new Random().Next(0,100);
        }
    }
}


